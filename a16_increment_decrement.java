public class a16_increment_decrement {
    public static void main(String[] args) {
        
        int x=25;
        int y;
        
        y=x++;
        System.out.println(y);
        
        y=x;
        System.out.println(y);
        
        y=--x;
        System.out.println(y);
        
        y=x;
        System.out.println(y);
        
        y=++x;
        System.out.println(y);
        
        y=x--;
        System.out.println(y);
        
        y=x;
        System.out.println(y);
    }
}
