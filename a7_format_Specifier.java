public class a7_format_Specifier {
    public static void main(String[] args) {
        
        int a=5;
        char c='z';
        
        boolean b=true;
        double d=5.5;
        float f=2.3f;
        
        System.out.printf("a=%d\n",a);
        System.out.printf("c=%c\n",c);
       
        System.out.printf("d=%f\n",d);
        System.out.printf("f=%.2f\n",f);
        System.out.printf("b=%b\n",b);
    }
    
}
